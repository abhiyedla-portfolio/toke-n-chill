#!/usr/bin/env node

const fs = require('node:fs');
const path = require('node:path');
const vm = require('node:vm');
const { spawnSync } = require('node:child_process');
const ts = require('typescript');

const rootDir = path.resolve(__dirname, '..');
const defaultOutputPath = path.join(
  rootDir,
  'cloudflare',
  'd1',
  'seed-product-metadata.sql',
);

function parseArgs(argv) {
  const options = {
    database: null,
    execute: false,
    local: false,
    out: defaultOutputPath,
    remote: false,
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];

    if (arg === '--out') {
      options.out = path.resolve(rootDir, argv[index + 1]);
      index += 1;
      continue;
    }

    if (arg === '--database' || arg === '--binding') {
      options.database = argv[index + 1];
      index += 1;
      continue;
    }

    if (arg === '--execute') {
      options.execute = true;
      continue;
    }

    if (arg === '--remote') {
      options.remote = true;
      continue;
    }

    if (arg === '--local') {
      options.local = true;
    }
  }

  return options;
}

function resolveModulePath(specifier, fromDir) {
  if (specifier.startsWith('@/')) {
    return resolveExistingModule(path.join(rootDir, 'src', specifier.slice(2)));
  }

  if (specifier.startsWith('./') || specifier.startsWith('../')) {
    return resolveExistingModule(path.resolve(fromDir, specifier));
  }

  return null;
}

function resolveExistingModule(basePath) {
  const candidates = [
    basePath,
    `${basePath}.ts`,
    `${basePath}.tsx`,
    `${basePath}.js`,
    path.join(basePath, 'index.ts'),
    path.join(basePath, 'index.tsx'),
    path.join(basePath, 'index.js'),
  ];

  for (const candidate of candidates) {
    if (fs.existsSync(candidate)) {
      return candidate;
    }
  }

  throw new Error(`Unable to resolve module from "${basePath}".`);
}

const moduleCache = new Map();

function loadTsModule(modulePath) {
  const resolvedPath = path.resolve(modulePath);

  if (moduleCache.has(resolvedPath)) {
    return moduleCache.get(resolvedPath).exports;
  }

  const source = fs.readFileSync(resolvedPath, 'utf8');
  const transpiled = ts.transpileModule(source, {
    compilerOptions: {
      esModuleInterop: true,
      jsx: ts.JsxEmit.ReactJSX,
      module: ts.ModuleKind.CommonJS,
      target: ts.ScriptTarget.ES2020,
    },
    fileName: resolvedPath,
  }).outputText;

  const module = { exports: {} };
  moduleCache.set(resolvedPath, module);

  const dirname = path.dirname(resolvedPath);
  const localRequire = (specifier) => {
    const localPath = resolveModulePath(specifier, dirname);

    if (localPath) {
      return loadTsModule(localPath);
    }

    return require(specifier);
  };

  const context = {
    Buffer,
    clearImmediate,
    clearInterval,
    clearTimeout,
    console,
    exports: module.exports,
    module,
    process,
    require: localRequire,
    setImmediate,
    setInterval,
    setTimeout,
    __dirname: dirname,
    __filename: resolvedPath,
  };

  vm.runInNewContext(transpiled, context, { filename: resolvedPath });

  return module.exports;
}

function escapeSqlString(value) {
  return value.replace(/'/g, "''");
}

function sqlValue(value) {
  if (value === null || value === undefined) {
    return 'NULL';
  }

  if (typeof value === 'number') {
    return String(value);
  }

  if (typeof value === 'boolean') {
    return value ? '1' : '0';
  }

  return `'${escapeSqlString(String(value))}'`;
}

function buildSeedSql(products) {
  const header = [
    '-- Generated by scripts/seed-d1-from-inventory.cjs',
    '-- Source: src/data/products.ts',
    `-- Generated at: ${new Date().toISOString()}`,
    '',
  ];

  const statements = products.map((product, index) => {
    const values = [
      sqlValue(product.slug),
      sqlValue(product.cloverId ?? null),
      sqlValue(product.name),
      sqlValue(product.brand),
      sqlValue(product.category),
      sqlValue(product.description || null),
      sqlValue(product.image || null),
      sqlValue(product.variants ? JSON.stringify(product.variants) : null),
      sqlValue(product.featured ? 1 : 0),
      sqlValue(product.newArrival ? 1 : 0),
      sqlValue(index),
      '1',
      '0',
    ];

    return [
      'INSERT INTO product_metadata (',
      '  slug,',
      '  clover_id,',
      '  name,',
      '  brand,',
      '  category,',
      '  description,',
      '  image,',
      '  variants_json,',
      '  featured,',
      '  new_arrival,',
      '  sort_order,',
      '  is_active,',
      '  hide_from_catalog',
      ') VALUES (',
      `  ${values.join(', ')}`,
      ')',
      'ON CONFLICT(slug) DO UPDATE SET',
      '  clover_id = excluded.clover_id,',
      '  name = excluded.name,',
      '  brand = excluded.brand,',
      '  category = excluded.category,',
      '  description = excluded.description,',
      '  image = excluded.image,',
      '  variants_json = excluded.variants_json,',
      '  featured = excluded.featured,',
      '  new_arrival = excluded.new_arrival,',
      '  sort_order = excluded.sort_order,',
      '  is_active = excluded.is_active,',
      '  hide_from_catalog = excluded.hide_from_catalog;',
    ].join('\n');
  });

  return [...header, ...statements, ''].join('\n');
}

function run() {
  const options = parseArgs(process.argv.slice(2));
  const { products } = loadTsModule(path.join(rootDir, 'src', 'data', 'products.ts'));
  const sql = buildSeedSql(products);

  fs.mkdirSync(path.dirname(options.out), { recursive: true });
  fs.writeFileSync(options.out, sql, 'utf8');

  console.log(`Wrote D1 seed SQL for ${products.length} products to ${options.out}`);

  if (!options.execute) {
    console.log('Run again with --execute --remote to import into Cloudflare D1.');
    return;
  }

  if (!options.database) {
    console.error('Missing database identifier. Re-run with --database <database-name-or-binding>.');
    process.exit(1);
  }

  const commandArgs = [
    'wrangler',
    'd1',
    'execute',
    options.database,
    '--file',
    options.out,
  ];

  if (options.remote) {
    commandArgs.push('--remote');
  }

  if (options.local) {
    commandArgs.push('--local');
  }

  const command = process.platform === 'win32' ? 'npx.cmd' : 'npx';
  const result = spawnSync(command, commandArgs, {
    cwd: rootDir,
    stdio: 'inherit',
  });

  if (result.status !== 0) {
    process.exit(result.status ?? 1);
  }
}

run();
